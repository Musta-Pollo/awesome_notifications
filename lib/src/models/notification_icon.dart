import 'package:logger/logger.dart';
import 'dart:io' as io;

class NotificationIcon {
  String iconDataToDrawableIconName(IconData? iconData) {
    var logger = Logger(
      printer: PrettyPrinter(
          colors: io.stdout.supportsAnsiEscapes,
          printEmojis: true,
          lineLength: io.stdout.terminalColumns),
    );

    if (iconData == null) {
      logger.e("iconDataToDrawableIconName - iconData is null");
      return "ac_unit";
    }

    if (iconData.fontFamily != "MaterialIcons") {
      logger.e("iconDataToDrawableIconName - Inserted not a material icon: " +
          iconData.toString());
    }
    logger.v("iconDataToDrawableIconName - Correct icon data: " +
        iconData.toString());

    final relative_path = "android/app/src/main/res/drawable/";
    final icon_name = codePointToIconName[iconData.codePoint] ?? "ac_unit";
    return relative_path + icon_name;
  }
}

